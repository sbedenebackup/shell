#include "libft.h"

int		ft_intlen(long long nb)
{
	int	len;

	len = nb < 0 ? 1 : 0;
	if (!nb)
		return (1);
	while (nb)
	{
		nb /= 10;
		len++;
	}
	return (len);
}
